	/**
	 * 解析请求信息
	 */
	public void parse() {
		int length = 0;
		byte[] bytes = new byte[BUFFER_SIZE];
		try {
			length = inputStream.read(bytes);
		} catch (IOException e) {
			e.printStackTrace();
		}
		StringBuilder sb = new StringBuilder();
		for(int i=0;i<length;i++) {
			sb.append((char)bytes[i]);
		}
		this.uri = parseUri(sb.toString());
	}
	
	/**
	 * 解析请求地址
	 * @param requestContent
	 * @return
	 */
	private String parseUri(String requestContent) {
		int index1,index2;
		// 第一个空格的位置
		index1 = requestContent.indexOf(' ');
		if(index1 != -1) {
			// 第二个空格的位置
			index2 = requestContent.indexOf(' ',index1 + 1);
			if(index2 > index1) {
				// 截取请求地址
				return requestContent.substring(index1 + 1,index2);
			}
		}
		return "";
	}

